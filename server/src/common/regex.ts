//Може містити літери, цифри, підкреслення і тире (3-16)
export const userNameRegex = /^[a-zA-Z0-9_-]{3,16}$/ 

 //Має містити принаймні одну велику літеру, одну малу літеру, одну цифру і один спеціальний символ (8-20)
export const passwordRegex = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*()])[A-Za-z\d!@#$%^&*()]{8,20}$/

export const emailRegex = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/